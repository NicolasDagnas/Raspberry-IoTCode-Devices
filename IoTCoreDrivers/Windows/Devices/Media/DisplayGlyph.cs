	//*********************************************************************************************
	// Structure DisplayGlyph
	//*********************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------
	/// <summary>
	/// Fournit un objet permettant de dessiner une image monochrome.
	/// </summary>
	//---------------------------------------------------------------------------------------------
	public struct DisplayGlyph
		{
		//*****************************************************************************************
		/// <summary>
		/// Contructeur statique de l'objet <b>DisplayGlyph</b>.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		static DisplayGlyph () { Empty = new DisplayGlyph (); }
		//*****************************************************************************************
				
		//*****************************************************************************************
		/// <summary>
		/// Initialise une nouvelle instance de l'objet <b>DisplayGlyph</b>.
		/// </summary>
		/// <param name="Offset">Index into DisplayFont.Bitmaps.</param>
		/// <param name="Height">Bitmap dimensions in pixels.</param>
		/// <param name="Width">Bitmap dimensions in pixels.</param>
		/// <param name="Advance">Distance to advance cursor (x axis).</param>
		//-----------------------------------------------------------------------------------------
		public DisplayGlyph ( uint[] Bitmap, int Width, int Height )
			{
			//-------------------------------------------------------------------------------------
			this.Bitmap = Bitmap;
			this.Width  = Width;
			this.Height = Height;
			//-------------------------------------------------------------------------------------
			}
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Glyph bitmaps, concatenated.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public uint[] Bitmap;
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Obtiens un objet <b>DisplayGlyph</b> non initialisé.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public static readonly DisplayGlyph Empty;
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Bitmap dimensions in pixels.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public int Height;
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Indique si la structure courante est initialisée ou non.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public bool IsEmpty { get { return ( this.Bitmap == null || this.Bitmap.Length == 0 ); } }
		//*****************************************************************************************
		
		//*****************************************************************************************
		/// <summary>
		/// Bitmap dimensions in pixels.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public int Width;
		//*****************************************************************************************
		}
	//---------------------------------------------------------------------------------------------
	#endregion

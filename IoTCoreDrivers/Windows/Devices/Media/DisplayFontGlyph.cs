	//*********************************************************************************************
	// Structure DisplayFontGlyph
	//*********************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------
	/// <summary>
	/// Définit un pointeur sur l'image du caractère.
	/// </summary>
	//---------------------------------------------------------------------------------------------
	public struct DisplayFontGlyph
		{
		//-----------------------------------------------------------------------------------------
		// Section des Attributs
		//-----------------------------------------------------------------------------------------
		private int offset;
		private int height;
		private int width;
		private int advance;
		private int horizontalOffset;
		private int verticalOffset;
		//-----------------------------------------------------------------------------------------
		
		//*****************************************************************************************
		/// <summary>
		/// Contructeur statique de l'objet <b>DisplayFontGlyph</b>.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		static DisplayFontGlyph () { Empty = new DisplayFontGlyph (); }
		//*****************************************************************************************
				
		//*****************************************************************************************
		/// <summary>
		/// Initialise une nouvelle instance de l'objet <b>DisplayFontGlyph</b>.
		/// </summary>
		/// <param name="Offset">Index into DisplayFont.Bitmaps.</param>
		/// <param name="Height">Bitmap dimensions in pixels.</param>
		/// <param name="Width">Bitmap dimensions in pixels.</param>
		/// <param name="Advance">Distance to advance cursor (x axis).</param>
		//-----------------------------------------------------------------------------------------
		public DisplayFontGlyph ( int Offset, int Width, int Height, int Advance )
			{
			//-------------------------------------------------------------------------------------
			this.offset           = Offset;
			this.height           = Height;
			this.width            = Width;
			this.advance          = Advance;
			this.horizontalOffset = 0;
			this.verticalOffset   = 0;
			//-------------------------------------------------------------------------------------
			}
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Initialise une nouvelle instance de l'objet <b>DisplayFontGlyph</b>.
		/// </summary>
		/// <param name="Offset">Index into DisplayFont.Bitmaps.</param>
		/// <param name="Height">Bitmap dimensions in pixels.</param>
		/// <param name="Width">Bitmap dimensions in pixels.</param>
		/// <param name="Advance">Distance to advance cursor (x axis).</param>
		/// <param name="HorizontalOffset">Distance from cursor pos to UL corner.</param>
		/// <param name="VerticalOffset">Distance from cursor pos to UL corner.</param>
		//-----------------------------------------------------------------------------------------
		public DisplayFontGlyph ( int Offset, int Width, int Height, int Advance, 
			                                             int HorizontalOffset, int VerticalOffset )
			{
			//-------------------------------------------------------------------------------------
			this.offset           = Offset;
			this.height           = Height;
			this.width            = Width;
			this.advance          = Advance;
			this.horizontalOffset = HorizontalOffset;
			this.verticalOffset   = VerticalOffset;
			//-------------------------------------------------------------------------------------
			}
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Distance to advance cursor (x axis).
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public int Advance { get { return this.advance; } }
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Obtiens un objet <b>DisplayFontGlyph</b> non initialisé.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public static readonly DisplayFontGlyph Empty;
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Bitmap dimensions in pixels.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public int Height { get { return this.height; } }
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Distance from cursor pos to UL corner.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public int HorizontalOffset { get { return this.horizontalOffset; } }
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Indique si la structure courante est initialisée ou non.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public bool IsEmpty { get { return ( this.height == 0 && this.width == 0 ); } }
		//*****************************************************************************************
		
		//*****************************************************************************************
		/// <summary>
		/// Index into DisplayFont.Bitmaps.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public int Offset { get { return this.offset; } }
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Distance from cursor pos to UL corner.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public int VerticalOffset { get { return this.verticalOffset; } }
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Bitmap dimensions in pixels.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public int Width { get { return this.width; } }
		//*****************************************************************************************
		}
	//---------------------------------------------------------------------------------------------
	#endregion
	//*********************************************************************************************

	} // Fin du namespace "Windows.Devices.Media"

	//*********************************************************************************************
	// Classe DisplayFont
	//*********************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------
	/// <summary>
	/// Définit une font à utiliser avec les objets de type <b>Windows.Devices.Gpio.Display</b>.
	/// </summary>
	//---------------------------------------------------------------------------------------------
	public class DisplayFont
		{
		//*****************************************************************************************
		/// <summary>
		/// Obtiens le glyph correspondant au caractère spécifié.
		/// </summary>
		/// <param name="Character">Caractère à chercher.</param>
		/// <returns>Objet <b>DisplayFontGlyph</b> correspondant.</returns>
		//-----------------------------------------------------------------------------------------
		internal DisplayFontGlyph GetGlyph ( char Character )
			{
			//-------------------------------------------------------------------------------------
			ushort Index = (ushort)( Character - this.First );
			
			if ( Index >= 0 && Index < this.Glyphs.Length ) return this.Glyphs[Index];

			return DisplayFontGlyph.Empty;
			//-------------------------------------------------------------------------------------
			}
		//*****************************************************************************************
		
		//*****************************************************************************************
		/// <summary>
		/// Glyph bitmaps, concatenated.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		internal uint[] Bitmaps;
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Obtient la hauteur en pixel, du carré cadratin pour le style spécifié. 
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public uint EmHeight { get; protected set; }
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// First caractère offset.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		internal uint First;
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Glyph array.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		internal DisplayFontGlyph[] Glyphs;
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Retourne l'interligne en unités de design. L'interligne est la distance verticale 
		/// séparant les lignes de base de deux lignes de texte consécutives.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public int LineSpacing;
		//*****************************************************************************************

		//*****************************************************************************************
		/// <summary>
		/// Indique le type de la Font.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public DisplayFontType Type { get; protected set; }
		//*****************************************************************************************
		
		//*****************************************************************************************
		/// <summary>
		/// Obtiens le nom de la police.
		/// </summary>
		//-----------------------------------------------------------------------------------------
		public string Name { get; protected set; }
		//*****************************************************************************************
		}
	//---------------------------------------------------------------------------------------------
	#endregion
	//*********************************************************************************************

	} // Fin du namespace "Windows.Devices.Media"
